import Foundation
import UIKit

// 创建 联系人 类
class ContactPerson {
    var name: String? = nil           // 姓名
    var sex: String?  = nil           // 性别
    var phoneNumber: String? = nil    // 电话号码
    var address: String? = nil        // 地址
    var content: String? = nil        // 简介
    var photoImage: UIImage? = nil    // 头像    // 需要导入UIKit框架
}